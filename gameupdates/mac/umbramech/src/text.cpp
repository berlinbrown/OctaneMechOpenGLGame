// The text texture only needs to be initialized once
static unsigned int fontID = 0;

// Forward definition
extern unsigned char fontData[];

#define FONT_W 8
#define FONT_H 8
#define FONT_DRAW_W 8
#define FONT_DRAW_H 9
#define FONT_TEX_W 64
#define FONT_TEX_H 128

//
// Reset_FontID
//
void Reset_FontID(void)
{
	fontID = 0;
} 

//---------------------------------------------------------------------------
// A text box constructor
//---------------------------------------------------------------------------
TextBoxPtr InitTextBox(int inL, int inR, int inT, int inB)
{

	TextBoxPtr text = NULL;

	text = (TextBoxPtr)malloc(sizeof(TextBox));

	// Calculate the Text box boundaries
	text->boxL = inL;
	text->boxR = inL + ((inR - inL) - ((inR - inL) % FONT_DRAW_W));
	text->boxT = inT;
	text->boxB = inT + ((inB - inT) - ((inB - inT) % FONT_DRAW_H));

	// Initialize the Buffers and Pointers
	text->buffer = (char *)malloc(MAX_STR);
	text->buffer[0] = '\0';

	text->drawBufferPtr = text->buffer;

	// Calculate the number of lines per Page
	text->pageLineCnt = (text->boxB - text->boxT) / FONT_DRAW_H;
	text->scrollCnt = 0;
	text->currX = text->boxL;

	if (!fontID)
	{

		// Create Texture
		glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
		glGenTextures(1, &fontID);
		glBindTexture(GL_TEXTURE_2D, fontID);

		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_REPEAT);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_REPEAT);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);

		glTexImage2D(

			GL_TEXTURE_2D,
			0,
			GL_LUMINANCE_ALPHA,
			FONT_TEX_W,
			FONT_TEX_H,
			0,
			GL_LUMINANCE_ALPHA,
			GL_UNSIGNED_BYTE,
			(void *)fontData);

	} 

	// Set Color
	SET_COLOR(text->color, 255, 255, 255, 255);

	// Set Mode
	text->mode = 0;

	return text;

} // end of the fnuction

//
// DestroyTextBox
//
void DestroyTextBox(TextBoxPtr text)
{
	RELEASE_OBJECT(text);
} 

void SetTextMode(TextBoxPtr text, unsigned int modeIn)
{
	text->mode = modeIn;
};

//---------------------------------------------------------------------------
// Load Font Texture and set current Matrix to Screen Space
//---------------------------------------------------------------------------
void TextBegin(TextBoxPtr text)
{

	// Push the neccessary Matrices on the stack
	glMatrixMode(GL_PROJECTION);
	glPushMatrix();
	glLoadIdentity();
	glOrtho(0.0, SCREEN_WIDTH, SCREEN_HEIGHT,
			0.0, -1.0, 1.0);

	glMatrixMode(GL_MODELVIEW);

	glPushMatrix();
	glLoadIdentity();

	// Push the neccessary Attributes on the stack
	glPushAttrib(GL_TEXTURE_BIT | GL_ENABLE_BIT);

	glBindTexture(GL_TEXTURE_2D, fontID);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_REPEAT);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_REPEAT);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
	glTexEnvf(GL_TEXTURE_ENV, GL_TEXTURE_ENV_MODE, GL_MODULATE);

	glEnable(GL_TEXTURE_2D);

	// Always Draw in Front
	glDisable(GL_DEPTH_TEST);
	glDisable(GL_CULL_FACE);

	glDisable(GL_LIGHTING);

	if (!(text->mode & TEXT_DRAW_BACKGROUND))
	{
		glEnable(GL_BLEND);
		glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
	}

} 

//
// SetColor
//
void SetTextColor(TextBoxPtr text, unsigned char r,
				  unsigned char g, unsigned char b)
{
	text->color.r = r;
	text->color.g = g;
	text->color.b = b;

} 

//---------------------------------------------------------------------------
// Remove Screen Matrix
//---------------------------------------------------------------------------
void TextEnd(void)
{

	// Return to previous Matrix and Attribute states. Easy cleanup!
	glMatrixMode(GL_PROJECTION);
	glPopMatrix();
	glMatrixMode(GL_MODELVIEW);
	glPopMatrix();
	glPopAttrib();

	glDisable(GL_TEXTURE_2D);
	glEnable(GL_LIGHTING);

} 

//---------------------------------------------------------------------------
// Draw a string at a given coordinate
//---------------------------------------------------------------------------
void DrawString(TextBoxPtr text, int x, int y, char *str)
{

	TextBegin(text);
	DrawStr(text, x, y, 0, 0, str);
	TextEnd();

} 

//---------------------------------------------------------------------------
// Draw the Text given to TextBoxPrintf()
//---------------------------------------------------------------------------
void DrawText(TextBoxPtr text)
{

	Paginate(text);

	TextBegin(text);

	if (text->mode & TEXT_DRAW_BOX)
		DrawBoundingBox(text);

	DrawStr(text, text->boxL, text->boxT, 1, text->boxB,
			text->drawBufferPtr);

	TextEnd();

} 

//---------------------------------------------------------------------------
// Draw a string at a given position in screen space
//---------------------------------------------------------------------------
void DrawStr(TextBoxPtr text,
			 int x, int y, int maxFlag, int maxY, char *string)
{
	int cursorX;
	int cursorY;

	char *cP = string;
	int index;
	int s, t;

	cursorX = x;
	cursorY = y;

	glColor4ub(text->color.r, text->color.g,
			   text->color.b, text->color.a);
	glBegin(GL_QUADS);

	// Parse the String
	while (*cP != '\0')
	{

		// Visible characters
		if (' ' == *cP && !(text->mode & TEXT_DRAW_SPACES))
		{
			cursorX += FONT_DRAW_W;
		}
		else if (' ' <= *cP && *cP <= '~')
		{

			index = *cP - ' ';
			s = (index % 8) * FONT_W;
			t = (index / 8) * FONT_H;

			// Actually render to screen --
			glTexCoord2f((float)(s) / FONT_TEX_W, (float)(t) / FONT_TEX_H);
			glVertex3s(cursorX, cursorY, 0);
			glTexCoord2f((float)(s) / FONT_TEX_W, (float)(t + FONT_H + 1) / FONT_TEX_H);
			glVertex3s(cursorX, cursorY + FONT_DRAW_H, 0);
			glTexCoord2f((float)(s + FONT_W) / FONT_TEX_W, (float)(t + FONT_H + 1) / FONT_TEX_H);
			glVertex3s(cursorX + FONT_DRAW_W, cursorY + FONT_DRAW_H, 0);
			glTexCoord2f((float)(s + FONT_W) / FONT_TEX_W, (float)(t) / FONT_TEX_H);
			glVertex3s(cursorX + FONT_DRAW_W, cursorY, 0);

			cursorX += FONT_DRAW_W;
		}

		if (*cP == '\n')
		{
			cursorX = x;
			cursorY += FONT_DRAW_H;
			if (maxFlag && cursorY + FONT_DRAW_H > maxY)
				break;
		}
		cP++;
	}

	glEnd();

} 

//---------------------------------------------------------------------------
// Draw Bounding Box
//---------------------------------------------------------------------------
void DrawBoundingBox(TextBoxPtr text)
{

	glColor4f(0.0, 0.0, 0.0, 1.0);
	glBegin(GL_QUADS);

	glVertex3s(text->boxL, text->boxT, 0);
	glVertex3s(text->boxR, text->boxT, 0);
	glVertex3s(text->boxR, text->boxB, 0);
	glVertex3s(text->boxL, text->boxB, 0);
	glEnd();

} 

//---------------------------------------------------------------------------
// This function will concatinate a formated string the a TextBox
//---------------------------------------------------------------------------
void FormatStrCat(TextBoxPtr text, char *str)
{
	char buff[MAX_STR];
	char buff2[MAX_STR];
	char *cP;
	char *cP2;

	strcpy(buff, str);
	cP = buff;

	// Format String for caption
	// Parse the String
	while (*cP != '\0')
	{
		// Visible characters
		if (' ' <= *cP && *cP <= '~')
		{
			text->currX += FONT_DRAW_W;
		}
		else if (*cP == '\n')
		{
			text->currX = text->boxL;
		}

		// Wrap
		if (text->currX >= text->boxR)
		{
			cP++;
			// Find the last space
			if (text->mode & TEXT_WRAP_SPACES)
			{
				while (cP > buff && *cP != ' ')
					cP--;
			}

			strcpy(buff2, cP);
			*cP++ = '\n';
			*cP = '\0';
			// Eliminate Space
			if (text->mode & TEXT_WRAP_SPACES && buff2[0] == ' ')
				cP2 = &buff2[1];
			else
				cP2 = buff2;
			strcat(cP, cP2);
			text->currX = text->boxL;
		}
		else
		{
			cP++;
		}
	}

	// Make sure it fits in the text buffer by removing
	// strings at the top of the buffer.
	cP = text->buffer;
	while (strlen(cP) + strlen(buff) > MAX_STR)
	{
		cP = strchr(cP, '\n');
		if (!cP)
		{
			assert(!"TextBox::FormatStrCat: string too long!!!");
		}
		cP++;
	}
	// If there are strings that need to be removed. Do so.
	if (cP != text->buffer)
	{
		strcpy(buff2, cP);
		strcpy(text->buffer, buff2);
	}
	// Now there is space. So, concatinate.
	strcat(text->buffer, buff);

} 

//---------------------------------------------------------------------------
// Paginate the text Box
//---------------------------------------------------------------------------
void Paginate(TextBoxPtr text)
{

	int newLineCnt = 0;
	int pageNewLineCnt;
	char *cP;

	// Calculate the number of new lines from the bottom we want
	pageNewLineCnt = text->pageLineCnt + text->scrollCnt;

	// Starting at the end of the string count the number of newlines backwards
	cP = strchr(text->buffer, '\0');
	while (cP > text->buffer)
	{
		if (*cP == '\n')
			newLineCnt++;
		if (newLineCnt == pageNewLineCnt)
		{
			cP++;
			break;
		}
		cP--;
	}
	if (cP == text->buffer && newLineCnt > text->pageLineCnt)
		text->scrollCnt = newLineCnt - text->pageLineCnt + 1;

	text->drawBufferPtr = cP;

} 

//---------------------------------------------------------------------------
// Printf
//---------------------------------------------------------------------------
void Printf(TextBoxPtr text, char *fmt, ...)
{
	va_list vlist;
	char buff[MAX_STR];

	// Get output string
	va_start(vlist, fmt);
	vsprintf(buff, fmt, vlist);

	FormatStrCat(text, buff);

	va_end(vlist);

} 

